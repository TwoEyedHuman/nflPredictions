drop table if exists method_definitions;
create table method_definitions (
	method_id int,
	method_definition varchar(1024),
    primary key (method_id)
);

drop table if exists nfl_schedule;
create table nfl_schedule (
	game_id int,
    home_team char(3),
    away_team char(3),
    primary key (game_id)
);

drop table if exists game_predictions;
create table game_predictions (
	seed INT,
    method_id int,
    game_id int,
    winner char(3),
    primary key (seed, method_id, game_id),
    foreign key (method_id) references method_definitions(method_id),
    foreign key (game_id) references nfl_schedule(game_id)
);

